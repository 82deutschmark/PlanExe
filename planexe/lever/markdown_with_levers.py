"""
Present the levers in a human readable format.
Where the vital few are presented first, and the non-vital at the end.

Input files:
- enrich_potential_levers_19dc0718-3df7-48e3-b06d-e2c664ecc07d.json
- focus_on_vital_few_levers_19dc0718-3df7-48e3-b06d-e2c664ecc07d.json

Output file:
- markdown_with_levers_19dc0718-3df7-48e3-b06d-e2c664ecc07d.md

PROMPT> python -m planexe.lever.markdown_with_levers
"""
import logging
from pydantic import BaseModel, Field
from typing import List, Dict, Any

logger = logging.getLogger(__name__)

class EnrichLever(BaseModel):
    """Represents a single lever generated by the enrich_potential_levers.py module."""
    lever_id: str
    name: str
    consequences: str
    options: List[str]
    review: str
    description: str
    synergy_text: str
    conflict_text: str

class VitalLever(BaseModel):
    """Represents a single lever generated by the focus_on_vital_few_levers.py module."""
    lever_id: str
    name: str
    consequences: str
    options: List[str]
    review: str
    description: str
    synergy_text: str
    conflict_text: str

class LeverAssessment(BaseModel):
    """Represents the assessment of a single lever from the focus_on_vital_few_levers.py module."""
    lever_id: str
    lever_name: str
    strategic_importance: str
    justification: str

class MarkdownWithLevers:
    def __init__(self, enrich_levers: List[Dict[str, Any]], vital_levers: List[Dict[str, Any]], lever_assessments: List[Dict[str, Any]] = None):
        # Convert dictionaries to Pydantic models
        self.enrich_levers = [EnrichLever(**lever) for lever in enrich_levers]
        self.vital_levers = [VitalLever(**lever) for lever in vital_levers]
        
        # Convert assessment data to Pydantic models
        self.lever_assessments = {}
        if lever_assessments:
            for assessment in lever_assessments:
                assessment_model = LeverAssessment(**assessment)
                self.lever_assessments[assessment_model.lever_id] = assessment_model
    
    def to_markdown(self) -> str:
        """Generate markdown content with vital levers first, then the rest."""
        rows = []
        
        # Add title
        rows.append("# Project Levers Analysis")
        
        # Add vital levers section
        rows.append("## Primary Decisions")
        rows.append("The vital few decisions that have the most impact.\n")
        
        for i, lever in enumerate(self.vital_levers):
            lever_index = i + 1
            rows.append(f"### Decision {lever_index}: {lever.name}")
            rows.append(f"**Lever ID:** {lever.lever_id}\n")
            rows.append(f"**The Core Decision:** {lever.description}\n")
            rows.append(f"**Why It Matters:** {lever.consequences}\n")
            rows.append("**Strategic Choices:**\n")
            for option in lever.options:
                rows.append(f"- {option}")
            rows.append(f"\n**Trade-Off / Risk:** {lever.review}\n")
            rows.append(f"**Strategic Connections:**\n")
            rows.append(f"**Synergy:** {lever.synergy_text}\n")
            rows.append(f"**Conflict:** {lever.conflict_text}\n")

            # Add assessment information if available
            if lever.lever_id in self.lever_assessments:
                assessment = self.lever_assessments[lever.lever_id]
                rows.append(f"**Justification:** *{assessment.strategic_importance}*, {assessment.justification}\n")
            
        # Filter out levers that are already in vital section
        vital_lever_ids = {lever.lever_id for lever in self.vital_levers}
        additional_levers = [lever for lever in self.enrich_levers if lever.lever_id not in vital_lever_ids]
        
        # logger.info(f"Filtered out {len(self.enrich_levers) - len(additional_levers)} duplicate levers from additional section.")
        
        # Add remaining levers section
        rows.append("---")
        rows.append("## Secondary Decisions")
        rows.append("These decisions are less significant, but still worth considering.\n")
        
        for i, lever in enumerate(additional_levers):
            lever_index = len(self.vital_levers) + i + 1
            rows.append(f"### Decision {lever_index}: {lever.name}")
            rows.append(f"**Lever ID:** {lever.lever_id}\n")
            rows.append(f"**The Core Decision:** {lever.description}\n")
            rows.append(f"**Why It Matters:** {lever.consequences}\n")
            rows.append("**Strategic Choices:**\n")
            for option in lever.options:
                rows.append(f"- {option}")
            rows.append(f"\n**Trade-Off / Risk:** {lever.review}\n")
            rows.append(f"**Strategic Connections:**\n")
            rows.append(f"**Synergy:** {lever.synergy_text}\n")
            rows.append(f"**Conflict:** {lever.conflict_text}\n")

            # Add assessment information if available
            if lever.lever_id in self.lever_assessments:
                assessment = self.lever_assessments[lever.lever_id]
                rows.append(f"**Justification:** *{assessment.strategic_importance}*, {assessment.justification}\n")
        
        return "\n".join(rows)
    
    def save_markdown(self, filename: str):
        """Save the markdown content to a file."""
        markdown_content = self.to_markdown()
        with open(filename, 'w', encoding='utf-8') as f:
            f.write(markdown_content)

